// Son necesarias tres cosas para que esto funcione: 

- el analizador de PHP (módulo CGI o servidor) >> programa que interpreta y ejecuta el código PHP en el servidor
- un servidor web >> software que gestiona las peticiones HTTP y sirve los archivos adecuados a los clientes (navegadores).
- un navegador web >> es el software que el usuario utiliza para acceder a las páginas webs

Es necesario ejecutar el servidor con una instalación de PHP conectada. 
Se puede acceder al resultado del programa de PHP con un navegador, viendo la página de PHP a través del servidor. 
Todo esto se puede ejecutar en su máquina si está experimentado con la programación de PHP.