PHP tal y como se conoce hoy en día es en realidad el sucesor de un producto llamado PHP/FI. 

Creado en 1994 por //* Rasmus Lerdorf, https://es.wikipedia.org/wiki/Rasmus_Lerdorf
la primera encarnación de PHP era un conjunto simple de ficheros binarios 
Common Gateway Interface (CGI) escritos en el lenguaje de programación C. 

Originalmente utilizado para rastrear visitas de su currículum online, llamó al conjunto de scripts "Personal Home Page Tools", 
más frecuentemente referenciado como "PHP Tools". 

Con el paso del tiempo se quiso más funcionalidad, y Rasmus reescribió PHP Tools, produciendo una implementación más grande y rica. 

Este nuevo modelo fue capaz de interaccionar con bases de datos, y mucho más, proporcionando un entorno de trabajo sobre cuyos 
usuarios podían desarrollar aplicaciones web dinámicas sencillas tales como libros de visitas. 

En junio de 1995, Rasmus » publicó el código fuente de PHP Tools, lo que permitió a los desarrolladores usarlo 
como considerasen apropiado. Esto también permitió -y animó- a los usuarios a proporcionar soluciones a 
los errores del código, y generalmente a mejorarlo.

